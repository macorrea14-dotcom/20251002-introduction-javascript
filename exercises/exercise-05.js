let suma = 0; //esta variable acumula la suma
for (let numeros = 1; numeros <= 20; numeros++) { //ciclo  para  recorrer del  1   al 20
    suma += numeros; //va sumando cada numero
  console.log(numeros); //muestra cada numero
}
console.log("la suma en total es:", suma); //muestra la suma en total de los numeros

//justificacion: use el ciclo for para recorrer los numeros y acumular la suma de todos los numeros