const lista = ["html","css","javascript","react"]; //arreglo de palabras

for (let i = 0; i < lista.length; i++) { //ciclo for para recorrer el arreglo
    if (lista[i] === "javascript") { //condicion para encontrar la palabra javascript
        console.log("¡Encontrado!") //muestra el mensaje si encuentra la palabra
    }
}

//justificacion: use un ciclo for y una condición if para buscar un valor dentro del arreglo